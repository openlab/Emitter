			<p>There are 3 ways Emitter can be useful to developers.</p>
			
			<p>1. The data used by Emitter has been gently 'lifted' from NPRI's site (mostly from the downloadable .mdb's), we figured out how the data was reported and integrated it with geospacial data & added our analyses). These datasets were loaded into <a href="http://www.datadotgc.ca/data/tag/emitter">DataDOTgc.ca catalogue</a> (tagged as 'emitter'), and made available as a download AND as APIs by using <a href="http://ogdisdk.cloudapp.net/" title="OGDI">OGDI</a> open source toolkit. </p>
			
			<p>The APIs to the NPRI data are available on <a href="http://datadotgc.cloudapp.net/DataCatalog/DataSetList">DataDOTgc's OGDI instance</a>. Filter by keyword "Emitter" to see all of the datasets that we used. </p>
			<br>
			<p>2. Emitter has been released as open source under the New BSD license. Source code can be downloaded from GitHub at: <a href="http://github.com/redbit/Emitter" title="Emitter on GitHub">github.com/redbit/Emitter</a>.</p>
			<br />
			<p>3. The Emitter API* is available for use for free and provides you with search capabilities and retrieval of an individual facility's details. Furthermore, there are two distinct HTTP API requests that can be made to interact with the data used on Emitter.ca.</p>
			<p class="spacer">&nbsp;</p>
			
			<p><i>*The Emitter API will be made available for public use in the near future.</i></p>
